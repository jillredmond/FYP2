package com.finalYearProject.studentlife.dto;

public class AssignmentDto2 {
	
	private long assignentId;
	private String assignmentTitle;
	private long subjectId;
	
	public AssignmentDto2() {
		super();
	}

	public long getAssignentId() {
		return assignentId;
	}

	public void setAssignentId(long assignentId) {
		this.assignentId = assignentId;
	}

	public String getAssignmentTitle() {
		return assignmentTitle;
	}

	public void setAssignmentTitle(String assignmentTitle) {
		this.assignmentTitle = assignmentTitle;
	}

	public long getSubjectId() {
		return subjectId;
	}

	public void setSubjectId(long subjectId) {
		this.subjectId = subjectId;
	}
	
	

}
