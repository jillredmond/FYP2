package com.finalYearProject.studentlife.dto;

public class DayDto {
	
	private String day;
	private boolean item;
	
	public DayDto() {
		super();
	}

	public String getDay() {
		return day;
	}

	public void setDay(String day) {
		this.day = day;
	}

	public boolean isItem() {
		return item;
	}

	public void setItem(boolean item) {
		this.item = item;
	}
	
	

}
